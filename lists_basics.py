## Lists

animals = ['tiger','lion','bird','fish']

# get the first item by index position
print(animals[0])

# insert new item at the first position
animals[0] = 'elephant'
print(animals)

# append an item at the end of the list
animals.append('monkey')
print(animals)

# remove item from the end of the list
animals.pop()
print(animals)

# sort list 
animals.sort()
print(animals)

# reverse list
animals.reverse()
print(animals)